package com.prism.to;


public class StudentTO {

	private String studentBioId;
	private String gradeId;
	private String customerId;
	private String studentName;
	private String subtest;
	private String lastNameCap;
	private String mosisId;
	
	/**
	 * @return the mosisId
	 */
	public String getMosisId() {
		return mosisId;
	}
	/**
	 * @param mosisId the mosisId to set
	 */
	public void setMosisId(String mosisId) {
		this.mosisId = mosisId;
	}
	public String getSubtest() {
		return subtest;
	}
	public void setSubtest(String subtest) {
		this.subtest = subtest;
	}
	public String getStudentBioId() {
		return studentBioId;
	}
	public void setStudentBioId(String studentBioId) {
		this.studentBioId = studentBioId;
	}
	public String getGradeId() {
		return gradeId;
	}
	public void setGradeId(String gradeId) {
		this.gradeId = gradeId;
	}
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getLastNameCap() {
		return lastNameCap;
	}
	public void setLastNameCap(String lastNameCap) {
		this.lastNameCap = lastNameCap;
	}
	

}
